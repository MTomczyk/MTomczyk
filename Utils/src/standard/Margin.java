public class Margin
{
    public double left = 0.0d;
    public double right = 1.0d;
    public double top = 0.0d;
    public double bottom = 1.0d;

    public Margin(double left, double right, double top, double bottom)
    {
        this.left = left;
        this.right = right;
        this.top = top;
        this.bottom = bottom;
    }

}
